# THE CONVERTER FUNCTIONS 

# celsius to fahrenheit
def cel_to_fah(c):
    return (c * 9/5) + 32

# fahrenheit to celsius
def fah_to_cel(f):
    return (f - 32) * 5/9

# miles to kilometers
def mil_to_km(mi):
    return mi * 1.60934

# kilometers to miles
def km_to_mil(km):
    return km / 1.60934

# pounds to kilograms
def lbs_to_kg(lbs):
    return lbs * 0.453592

# kilograms to pounds
def kg_to_lbs(kg):
    return kg / 0.453592
